public class BiologicalRelation {
    int removal;
    int cousinship;

    public int getRemoval() {
        return removal;
    }

    public void setRemoval(int removal) {
        this.removal = removal;
    }

    public int getCousinship() {
        return cousinship;
    }

    public void setCousinship(int cousinship) {
        this.cousinship = cousinship;
    }
}
